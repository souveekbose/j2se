/**
 * Program to demonstrate primitive datatypes in Java
 */
public class Datatypes  {
    public static void main(String[] args) {
        //int: used for integers
        int int1 = 20;
        int int2 = -5;
        int intSum = int1 + int2;
        System.out.println("Sum of integers: " + intSum);

        //float: Decimal representation
        float float1 = 20.0F;
        float float2 = -5.0F;
        float floatSum = float1 + float2;
        System.out.println("Sum of float: " + floatSum);

        //float: Double representation
        double double1 = 20.0;
        double double2 = -5.0;
        double doubleSum = double1 + double2;
        System.out.println("Sum of double: " + doubleSum);

        //float: String representation
        String str1 = "20";
        String str2 = "5";
        String strSum = str1 + str2;
        System.out.println("Sum of string: " + strSum);

    }
}